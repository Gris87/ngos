#ifndef OS_SHARED_UEFIBASE_TEST_BITS64_SECTIONS_SECTION0___INCLUDE_ELF_RELA_H
#define OS_SHARED_UEFIBASE_TEST_BITS64_SECTIONS_SECTION0___INCLUDE_ELF_RELA_H



#include <buildconfig.h>
#include <elf/rela.h>
#include <uefibase/test/bits64/testengine.h>



#if NGOS_BUILD_TEST_MODE == OPTION_YES



TEST_CASES(section0, __include_elf_rela);
{
    TEST_CASE("ELF_RELA_BIND()");
    {
        TEST_ASSERT_EQUALS(ELF_RELA_SYMBOL(1311768467463790320), 305419896);
        TEST_ASSERT_EQUALS(ELF_RELA_SYMBOL(2541551403008843505), 591751049);
        TEST_ASSERT_EQUALS(ELF_RELA_SYMBOL(3771334300722392850), 878082192);
    }
    TEST_CASE_END();



    TEST_CASE("ELF_RELA_TYPE()");
    {
        TEST_ASSERT_EQUALS(ELF_RELA_TYPE(1311768467463790320), 2596069104);
        TEST_ASSERT_EQUALS(ELF_RELA_TYPE(2541551403008843505), 180150001);
        TEST_ASSERT_EQUALS(ELF_RELA_TYPE(3771334300722392850), 2882400018);
    }
    TEST_CASE_END();



    TEST_CASE("ELF_RELA_INFO()");
    {
        TEST_ASSERT_EQUALS(ELF_RELA_INFO(305419896ULL, 2596069104), 1311768467463790320);
        TEST_ASSERT_EQUALS(ELF_RELA_INFO(591751049ULL, 180150001),  2541551403008843505);
        TEST_ASSERT_EQUALS(ELF_RELA_INFO(878082192ULL, 2882400018), 3771334300722392850);
    }
    TEST_CASE_END();
}
TEST_CASES_END();



#endif



#endif // OS_SHARED_UEFIBASE_TEST_BITS64_SECTIONS_SECTION0___INCLUDE_ELF_RELA_H
