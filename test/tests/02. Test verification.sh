#!/bin/bash



###########################################################################################
#    PARAMETERS
###########################################################################################



WORKING_DIR=`pwd`



###########################################################################################
#    PROCESSING
###########################################################################################



echo -e "\e[36m==================================================\e[0m"
echo -e "\e[36m              Test: Test verification\e[0m"
echo -e "\e[36m==================================================\e[0m"



cd ../../
make verify-tests
VERIFICATION_RESULT=$?
cd ${WORKING_DIR}/



if [ ${VERIFICATION_RESULT} -ne 0 ]; then
    exit 1
fi



exit 0
